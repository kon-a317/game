// x座標よりマップの添え字番号を得る
int GetMapIndexX(float x)
{
	return (int)(x / MAP_BLOCK_SIZE);
}
// y座標よりマップの添え字番号を得る
int GetMapIndexY(float y)
{
	return (int)(y / MAP_BLOCK_SIZE);
}


// x,y座標よりマップ種類を調べる
int GetMapType(float x, float y, int stage)
{
	// 画面範囲内の時はマップ種類を返す
	if ((x > 0 && x < MAP_BLOCK_SIZE * MAP_BLOCK_X) &&
		(y > 0 && y < MAP_BLOCK_SIZE * MAP_BLOCK_Y))
	{
		return g_mapchip_data[stage][GetMapIndexY(y)][GetMapIndexX(x)];
	}
	// 画面範囲外は-1を返す。そもそもマップ内にキャラがいない。
	return -1;
}

void InitMap(Map& map)
{
	LoadDivGraph("texture/map2.png", 6, 6, 1, 64, 64, map.mapGraph);
}

//マップ描画
void DrawMap(Map& map, int stage)
{
	for (int iy = 0; iy < MAP_BLOCK_Y; iy++)
	{
		for (int ix = 0; ix < MAP_BLOCK_X; ix++)
		{
			DrawGraph(ix*MAP_BLOCK_SIZE,
				iy*MAP_BLOCK_SIZE,
				map.mapGraph[g_mapchip_data[stage][iy][ix]], TRUE);
		}
	}
}